let currentAzkarCategory = 'sabah';

function syncAzkar() {
  const container = document.getElementById("azkar-list-container");
  if (!container) return;

  const progress = getAzkarProgress();
  const list = azkarData[currentAzkarCategory];

  container.innerHTML = list.map(item => {
    const count = progress[item.id] || 0;
    const isCompleted = count >= item.repeat;
    
    return `
      <div class="ottoman-card ${isCompleted ? 'completed' : ''}" 
           style="padding: 25px 20px; margin-bottom: 15px; cursor: pointer;"
           onclick="tapAzkar('${item.id}', this)">
        
        <div class="ornament-right">❈</div>
        
        <div class="ottoman-text">
          ${item.text}
        </div>
        
        <div style="display: flex; justify-content: space-between; align-items: center; margin-top: 15px; position: relative; z-index: 2;">
          <div style="font-size: 0.8rem; color: var(--text-muted);">
            التكرار المطلوب: <span style="color: #c9943a; font-weight: bold;">${ar(item.repeat)}</span>
          </div>
          
          <div class="azkar-badge" style="background: ${isCompleted ? '#2d9e5f' : '#c9943a'}; color: #000; padding: 5px 15px; border-radius: 12px; font-size: 0.9rem; font-weight: bold; transition: 0.3s;">
            ${ar(count)} / ${ar(item.repeat)}
          </div>
        </div>
        
        <!-- Progress Bar -->
        <div class="ottoman-progress ${isCompleted ? 'completed' : ''}" style="width: ${(count / item.repeat) * 100}%;"></div>
      </div>
    `;
  }).join("");

  // Update tabs style
  const sabahBtn = document.getElementById("tab-sabah");
  const masaaBtn = document.getElementById("tab-masaa");
  if (sabahBtn && masaaBtn) {
    sabahBtn.classList.toggle("active", currentAzkarCategory === 'sabah');
    masaaBtn.classList.toggle("active", currentAzkarCategory === 'masaa');
  }
}

function tapAzkar(id, el) {
  const progress = getAzkarProgress();
  const list = azkarData[currentAzkarCategory];
  const item = list.find(x => x.id === id);
  
  if (!item) return;
  
  let count = progress[id] || 0;
  if (count < item.repeat) {
    count++;
    progress[id] = count;
    saveAzkarProgress(progress);
    
    // Play sound if available in global scope
    if (typeof playSound === 'function') {
      if (count === item.repeat) {
        playSound("complete");
        if ("vibrate" in navigator) navigator.vibrate([100, 50, 100]);
        if (typeof spawnParticles === 'function' && el) {
          spawnParticles(el);
        }
      } else {
        playSound("tap");
        if ("vibrate" in navigator) navigator.vibrate(30);
      }
    }
    
    syncAzkar();
  }
}

function switchAzkarTab(category) {
  currentAzkarCategory = category;
  syncAzkar();
}

function resetAzkarProgress() {
  if (confirm("هل تريد تصفير تقدم الأذكار الحالي؟")) {
    localStorage.removeItem("million_azkar_progress");
    syncAzkar();
    toast("تم التصفير ↺");
  }
}

function getAzkarProgress() {
  const data = localStorage.getItem("million_azkar_progress");
  if (data) {
    try {
      return JSON.parse(data);
    } catch (e) {
      console.error("Failed to parse azkar progress:", e);
      return {};
    }
  }
  return {};
}

function saveAzkarProgress(progress) {
  localStorage.setItem("million_azkar_progress", JSON.stringify(progress));
}

